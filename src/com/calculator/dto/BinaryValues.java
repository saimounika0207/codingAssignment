package com.calculator.dto;

public class BinaryValues {

	private Double value1;
	private Double value2;

	public BinaryValues(Double value1, Double value2) {
		super();
		this.value1 = value1;
		this.value2 = value2;
	}

	public Double getValue1() {
		return value1;
	}

	public Double getValue2() {
		return value2;
	}

}
